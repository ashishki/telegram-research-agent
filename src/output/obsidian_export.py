def _source_channel(url: str) -> str:
    match = re.match(r"https?://t\.me/([^/\s]+)", str(url or ""))
    if match:
        return match.group(1)
    host = re.sub(r"^https?://", "", str(url or "")).split("/", maxsplit=1)[0]
    return host or "unknown"


def _eligible_read_queue_atoms(threads: list[dict], report_contract: dict | None = None) -> list[dict]:
    report_contract = report_contract or {}
    decision_atom_ids = _contract_atom_ids(report_contract)
    atoms = []
    seen: set[int] = set()
    for atom in _all_atoms(threads):
        atom_id = _atom_id(atom)
        if not atom_id or atom_id in seen:
            continue
        seen.add(atom_id)
        if atom.get("atom_type") not in READ_QUEUE_TYPES:
            continue
        if not atom.get("source_urls"):
            continue
        if not (
            atom_id in decision_atom_ids
            or (_atom_is_current(atom) and _atom_is_high_signal(atom))
        ):
            continue
        atoms.append(atom)
    atoms.sort(
        key=lambda atom: (
            int(_atom_id(atom) in decision_atom_ids),
            float(atom.get("practical_utility_score") or 0.0),
            float(atom.get("novelty_score") or 0.0),
            float(atom.get("confidence") or 0.0),
            str(atom.get("last_seen_at") or ""),
        ),
        reverse=True,
    )
    return atoms[:MAX_READ_QUEUE_NOTES]


