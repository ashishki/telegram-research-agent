    strategy_note = _strategy_review_note(clean_week, report_path, strategy_review)
    notes.extend(tool_model_notes)
    notes.extend(practice_notes)
    notes.extend(channel_notes)
    notes.extend(read_queue_notes)
    notes.extend(experiment_notes)
    notes.extend([try_build_note, project_watch_note, feedback_note, strategy_note])

    written: list[Path] = []
    for relative_path, content in notes:
        target = root / relative_path
        _write_generated_note(target, content)
        written.append(target)

    atoms = _all_atoms(all_threads)
    summary = ObsidianExportSummary(
        week_label=clean_week,
        vault_root=str(root),
        files_written=len(written),
        thread_count=len(threads),
        source_atom_count=len(atoms),
        term_note_count=len(tool_model_notes) + len(practice_notes),
        channel_note_count=len(channel_notes),
        read_queue_note_count=len(read_queue_notes),
        experiment_note_count=len(experiment_notes),
        try_build_note_count=1,
        project_watch_note_count=1,
        feedback_summary_note_count=1,
        strategy_review_note_count=1,
    )
    _manifest(root, clean_week, written, summary.__dict__)
    return summary
