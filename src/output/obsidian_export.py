def _term_eligibility_reasons(term: str, atoms: list[dict], threads: list[dict], report_contract: dict) -> list[str]:
    reasons: list[str] = []
    atom_ids = {_atom_id(atom) for atom in atoms if _atom_id(atom)}
    channels = {_source_channel(url) for atom in atoms for url in atom.get("source_urls") or []}
    if atom_ids & _contract_atom_ids(report_contract) or _term_matches_action(term, _action_text_terms(report_contract)):
        reasons.append("decision_relevant")
    if any(str(thread.get("status") or "") in PROMOTED_THREAD_STATUSES for thread in threads):
        reasons.append("promoted")
    if len(atom_ids) >= 2 or len(channels) >= 2:
        reasons.append("repeated")
    if any(_atom_is_current(atom) and _atom_is_high_signal(atom) for atom in atoms):
        reasons.append("active")
    return reasons


def _term_matches_action(term: str, action_terms: set[str]) -> bool:
    clean = " ".join(str(term or "").strip().lower().split())
    if not clean:
        return False
    slug = _slug(clean)
    if clean in action_terms or slug in action_terms:
        return True
    return any(token in action_terms for token in re.findall(r"\b[a-zA-Z][a-zA-Z0-9_-]{2,}\b", clean))


