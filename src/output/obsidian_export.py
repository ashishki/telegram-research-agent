def _write_generated_note(path: Path, content: str) -> None:
    if path.exists():
        existing = path.read_text(encoding="utf-8")
        if GENERATED_MARKER not in existing:
            raise ObsidianExportError(f"refusing to overwrite hand-authored note: {path}")
    validate_generated_markdown(content, path=path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def validate_generated_markdown(content: str, *, path: Path | None = None) -> None:
    label = str(path) if path else "generated note"
    if not content.startswith("---\n"):
        raise ObsidianExportError(f"{label}: missing frontmatter")
    if GENERATED_MARKER not in content:
        raise ObsidianExportError(f"{label}: missing generated marker")
    if "[[" not in content:
        raise ObsidianExportError(f"{label}: missing Obsidian backlink")
    if "## Source References" not in content:
        raise ObsidianExportError(f"{label}: missing source references section")
    lowered = content.lower()
    if "raw_json" in lowered or "raw_posts" in lowered:
        raise ObsidianExportError(f"{label}: raw Telegram storage details leaked into vault note")


