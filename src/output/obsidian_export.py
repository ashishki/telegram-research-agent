def _utc_now_iso() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")


def _slug(value: object) -> str:
    text = str(value or "").strip().lower()
    text = re.sub(r"[^a-z0-9]+", "-", text).strip("-")
    return text or "untitled"


def _vault_root(vault_path: str | Path | None, namespace: str | None) -> Path:
    raw_root = Path(vault_path or os.environ.get("AI_INTELLIGENCE_VAULT_PATH", "") or DEFAULT_VAULT_PATH)
    root = raw_root.expanduser()
    ns = str(namespace or "").strip().strip("/")
    if not ns:
        return root
    ns_path = Path(ns)
    if ns_path.is_absolute() or any(part == ".." for part in ns_path.parts):
        raise ObsidianExportError("namespace must be a relative path without '..'")
    return root / ns_path


def _json_array(values: list | tuple) -> str:
    return json.dumps(list(values), ensure_ascii=False)


def _frontmatter(fields: dict[str, object]) -> str:
    lines = ["---"]
    for key, value in fields.items():
        if isinstance(value, (list, tuple)):
            rendered = _json_array(value)
        elif isinstance(value, bool):
            rendered = "true" if value else "false"
        elif value is None:
            rendered = "null"
        elif isinstance(value, (int, float)):
            rendered = str(value)
        else:
            rendered = json.dumps(str(value), ensure_ascii=False)
        lines.append(f"{key}: {rendered}")
    lines.append("---")
    return "\n".join(lines)


def _note(
    *,
    note_type: str,
    title: str,
    body: str,
    extra_frontmatter: dict[str, object] | None = None,
) -> str:
    frontmatter = {
        "type": note_type,
        "title": title,
        "generated": True,
        "generated_from": "telegram-research-agent",
        "generated_at": _utc_now_iso(),
    }
    frontmatter.update(extra_frontmatter or {})
    return f"{_frontmatter(frontmatter)}\n{GENERATED_MARKER}\n\n# {title}\n\n{body.rstrip()}\n"


def _wiki(path_without_suffix: str, label: str | None = None) -> str:
    clean = path_without_suffix.strip("/")
    if label:
        return f"[[{clean}|{label}]]"
    return f"[[{clean}]]"


def _source_refs(urls: list[str], *, fallback: str = "No source references available yet.") -> str:
    unique = []
    for url in urls:
        clean = str(url or "").strip()
        if clean and clean not in unique:
            unique.append(clean)
    lines = ["## Source References"]
    if unique:
        lines.extend(f"- {url}" for url in unique)
    else:
        lines.append(f"- {fallback}")
    return "\n".join(lines)


def _all_atoms(threads: list[dict]) -> list[dict]:
    atoms = []
    seen = set()
    for thread in threads:
        for atom in thread.get("atoms") or []:
            if atom["id"] in seen:
                continue
            seen.add(atom["id"])
            atoms.append(atom)
    return atoms


def _all_source_urls(threads: list[dict]) -> list[str]:
    return [url for atom in _all_atoms(threads) for url in atom.get("source_urls") or []]


def _atom_id(atom: dict) -> int:
    try:
        return int(atom.get("id") or 0)
    except (TypeError, ValueError):
        return 0


def _thread_atom_ids(thread: dict) -> set[int]:
    return {_atom_id(atom) for atom in thread.get("atoms") or [] if _atom_id(atom)}


def _contract_atom_ids(report_contract: dict) -> set[int]:
    ids: set[int] = set()
    for section in ("decision_cards", "claim_cards"):
        for card in report_contract.get(section) or []:
            if not isinstance(card, dict):
                continue
            if section == "claim_cards" and not (
                bool(card.get("decision_eligible"))
                or bool(card.get("quote_verified"))
                or str(card.get("evidence_tier") or "").startswith("verified")
            ):
                continue
            for value in card.get("evidence_atom_ids") or []:
                try:
                    atom_id = int(value)
                except (TypeError, ValueError):
                    continue
                if atom_id:
                    ids.add(atom_id)
    return ids


def _project_threads(report_contract: dict) -> set[str]:
    slugs: set[str] = set()
    diagnostic = report_contract.get("project_diagnostic") or {}
    for key in ("confirmed_leads", "project_watch"):
        for item in diagnostic.get(key) or []:
            if not isinstance(item, dict):
                continue
            confidence = str(item.get("confidence") or "")
            if key == "project_watch" and confidence not in {"medium", "higher"}:
                continue
            slug = str(item.get("thread_slug") or "").strip()
            if slug:
                slugs.add(slug)
    return slugs


def _eligible_project_watch(report_contract: dict) -> list[dict]:
    diagnostic = report_contract.get("project_diagnostic") or {}
    items: list[dict] = []
    for key in ("confirmed_leads", "project_watch"):
        for item in diagnostic.get(key) or []:
            if not isinstance(item, dict):
                continue
            confidence = str(item.get("confidence") or "")
            evidence_urls = [url for url in item.get("evidence_urls") or [] if str(url).strip()]
            if key == "confirmed_leads":
                if confidence != "higher" or len(evidence_urls) < 2:
                    continue
            elif confidence not in {"medium", "higher"}:
                continue
            items.append({**item, "watch_tier": "confirmed" if key == "confirmed_leads" else "watch"})
    return items[:4]


def _action_text_terms(report_contract: dict) -> set[str]:
    terms: set[str] = set()
    for action in report_contract.get("action_cards") or []:
        if not isinstance(action, dict):
            continue
        text = " ".join(
            str(action.get(key) or "")
            for key in ("title", "next_step", "success_criterion", "scope")
        )
        terms.update(re.findall(r"\b[a-zA-Z][a-zA-Z0-9_-]{2,}\b", text.lower()))
    for item in _eligible_project_watch(report_contract):
        for term in item.get("shared_terms") or []:
            clean = str(term or "").strip().lower()
            if clean:
                terms.add(clean)
    return terms


def _atom_is_current(atom: dict) -> bool:
    return str(atom.get("staleness_status") or "active") in CURRENT_ATOM_STATUSES


def _atom_is_high_signal(atom: dict) -> bool:
    return (
        float(atom.get("practical_utility_score") or 0.0) >= 0.75
        or float(atom.get("novelty_score") or 0.0) >= 0.75
        or float(atom.get("confidence") or 0.0) >= 0.8
    )


def _thread_eligibility_reasons(thread: dict, report_contract: dict) -> list[str]:
    reasons: list[str] = []
    atom_ids = _thread_atom_ids(thread)
    if atom_ids & _contract_atom_ids(report_contract) or str(thread.get("slug") or "") in _project_threads(report_contract):
        reasons.append("decision_relevant")
    if str(thread.get("status") or "") in PROMOTED_THREAD_STATUSES:
        reasons.append("promoted")
    if int(thread.get("atom_count") or len(thread.get("atoms") or [])) >= 2 or int(thread.get("source_channel_count") or 0) >= 2:
        reasons.append("repeated")
    if (
        str(thread.get("status") or "") in {"active", "production_pattern"}
        and bool(thread.get("changed_this_week"))
        and any(_atom_is_current(atom) and _atom_is_high_signal(atom) for atom in thread.get("atoms") or [])
    ):
        reasons.append("active")
    return list(dict.fromkeys(reasons))


def _thread_score(thread: dict, report_contract: dict) -> tuple[int, int, int, int, float, int, int, str]:
    reasons = set(_thread_eligibility_reasons(thread, report_contract))
    return (
        int("decision_relevant" in reasons),
        int("promoted" in reasons),
        int("repeated" in reasons),
        int("active" in reasons),
        float(thread.get("momentum_30d") or 0.0),
        int(thread.get("source_channel_count") or 0),
        int(thread.get("atom_count") or len(thread.get("atoms") or [])),
        str(thread.get("last_seen_at") or ""),
    )


def _selected_threads(threads: list[dict], report_contract: dict) -> list[dict]:
    eligible = [thread for thread in threads if _thread_eligibility_reasons(thread, report_contract)]
    eligible.sort(key=lambda thread: _thread_score(thread, report_contract), reverse=True)
    return eligible[:MAX_THREAD_NOTES]


def _weekly_threads(threads: list[dict], report_contract: dict) -> list[dict]:
    selected = list(threads)
    selected.sort(key=lambda thread: _thread_score(thread, report_contract), reverse=True)
    return [thread for thread in selected if _thread_score(thread, report_contract)[0:4] != (0, 0, 0, 0)][:MAX_WEEKLY_THREAD_LINKS]


